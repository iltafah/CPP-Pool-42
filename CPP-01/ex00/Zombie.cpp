#include "Zombie.hpp"

/*
** ************************************************************************** **
							Default Constructor								
** ************************************************************************** **
*/

Zombie::Zombie()
{
	this->name = "unnamed";
}

/*
** ************************************************************************** **
				Parameterized Constructor(std::string name)							
** ************************************************************************** **
*/

Zombie::Zombie(std::string name)
{
	this->name = name;
}

/*
** ************************************************************************** **
								Destructor			
** ************************************************************************** **
*/

Zombie::~Zombie()
{
	std::cout << RED;
	std::cout << this->name << " zombie has been destroyed" << std::endl;
	std::cout << RESET;
}

/*
** ************************************************************************** **
							Zombie::announce(void)		
** ************************************************************************** **
*/

void	Zombie::announce(void)
{
	std::cout << GREEN;
	std::cout << this->name << " BraiiiiiiinnnzzzZ..." << std::endl;
	std::cout << RESET;
	return ;
}

